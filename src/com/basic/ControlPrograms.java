package com.basic;

public class ControlPrograms {
public static void main(String[]args)
{
	int chioce = 100;
	switch(chioce) {
	case 100:
		System.out.println(100);
		break;
	case 200:
		System.out.println(200);
		break;
	case 300:
		System.out.println(300);
		break;
		default:
			System.out.println("default");
			break;
			
	}
	
}
}
